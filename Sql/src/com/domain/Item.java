package com.domain;

public class Item {
	
	private int codigo;
	private String descripcion;
	private String tipoLinea;
	private long cantPrimaria;
	private long cantSecundaria;
	private long cantPrimComp;
	private long cantSecComp;
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getTipoLinea() {
		return tipoLinea;
	}
	public void setTipoLinea(String tipoLinea) {
		this.tipoLinea = tipoLinea;
	}
	public long getCantPrimaria() {
		return cantPrimaria;
	}
	public void setCantPrimaria(long cantPrimaria) {
		this.cantPrimaria = cantPrimaria;
	}
	public long getCantSecundaria() {
		return cantSecundaria;
	}
	public void setCantSecundaria(long cantSecundaria) {
		this.cantSecundaria = cantSecundaria;
	}
	public long getCantPrimComp() {
		return cantPrimComp;
	}
	public void setCantPrimComp(long cantPrimComp) {
		this.cantPrimComp = cantPrimComp;
	}
	public long getCantSecComp() {
		return cantSecComp;
	}
	public void setCantSecComp(long cantSecComp) {
		this.cantSecComp = cantSecComp;
	}
	
	
	
	
}
