package com.domain;

public class Mesa {

	private int nroMesa;
	private int cantSilla;
	private String estado;
	
	public int getNroMesa() {
		return nroMesa;
	}
	public void setNroMesa(int nroMesa) {
		this.nroMesa = nroMesa;
	}
	public int getCantSilla() {
		return cantSilla;
	}
	public void setCantSilla(int cantSilla) {
		this.cantSilla = cantSilla;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	
	
}
